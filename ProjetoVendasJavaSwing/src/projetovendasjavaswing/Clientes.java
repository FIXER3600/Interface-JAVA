    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       DefaultTableModel model = new DefaultTableModel();
       model.setColumnIdentifiers(new String[]{"CPF","Nome"});
       RepositorioCliente rep = RepositorioCliente.obterInstancia();
       this.lista = rep.listarTodos();
       for(int i = 0;i < lista.size(); i++){
        model.addRow(new Object[]{this.lista.get(i).getCpf(),this.lista.get(i).getNome()});
    }
       jTable1.setModel( model);
    }//GEN-LAST:event_jButton1ActionPerformed

