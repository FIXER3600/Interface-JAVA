public void centralizeFrame(){
        int x,y;
        Rectangle scr = this.getGraphicsConfiguration().getBounds();
        Rectangle form = this.getBounds();
        x = (int) (scr.getWidth() - form.getWidth())/2;
        y = (int) (scr.getHeight() - form.getHeight()) / 2;
    this.setLocation(x,y);
    }
